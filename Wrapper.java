class Wrapper{
    public static void main(String[] args) {
        Integer a=5;
        Double b=7.7;
        Character c='A';
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(a.intValue());
        System.out.println(b.doubleValue());
        System.out.println(c.charValue());
    }
}