public class Assignment_Operators {
    public static void main(String[] args) {
        int n1=10,n2=20,n3;
        System.out.println(n3=n1);
        System.out.println(n2+=n1);
        System.out.println(n2-=n1);
        System.out.println(n2*=n1);
        System.out.println(n2/=n1);
        System.out.println(n2^=n1);
        System.out.println(n2%=n1);
        System.out.println(n3);
    }
}
